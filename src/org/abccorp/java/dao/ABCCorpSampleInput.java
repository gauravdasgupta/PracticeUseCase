package org.abccorp.java.dao;

public class ABCCorpSampleInput {
	
	private String fin_type;
	private String sub_type;
	private String tier;
	private String vendor;
	private String rating;
	private double wty;
	private double sox;
	private double amount;
	
	public String getFin_type() {
		return fin_type;
	}
	public void setFin_type(String fin_type) {
		this.fin_type = fin_type;
	}
	public String getSub_type() {
		return sub_type;
	}
	public void setSub_type(String sub_type) {
		this.sub_type = sub_type;
	}
	public String getTier() {
		return tier;
	}
	public void setTier(String tier) {
		this.tier = tier;
	}
	public String getVendor() {
		return vendor;
	}
	public void setVendor(String vendor) {
		this.vendor = vendor;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public double getWty() {
		return wty;
	}
	public void setWty(double wty) {
		this.wty = wty;
	}
	public double getSox() {
		return sox;
	}
	public void setSox(double sox) {
		this.sox = sox;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}

}
